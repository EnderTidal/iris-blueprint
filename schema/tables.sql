CREATE TABLE work_list (
  id            SERIAL PRIMARY KEY,
  system        VARCHAR(20) NOT NULL,
  category      VARCHAR(20) NOT NULL,
  title         TEXT NOT NULL,
  description   TEXT,
  priority      INTEGER DEFAULT 3,
  status        VARCHAR(20) DEFAULT 'open',
