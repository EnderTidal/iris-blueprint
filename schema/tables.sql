-- ============================================================
-- CONVENIENCE FUNCTION — Secret decryption
-- ============================================================

CREATE OR REPLACE FUNCTION decrypt_secret(val BYTEA, pass TEXT)
RETURNS TEXT AS $$
  SELECT pgp_sym_decrypt(val, pass);
$$ LANGUAGE SQL;
