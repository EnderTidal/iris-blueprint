COMMENT ON TABLE work_list IS 'Central work tracking. Items with shift=night and manifest_path are eligible for overnight autonomous execution.';
COMMENT ON COLUMN work_list.system IS 'Which system owns this work item';
COMMENT ON COLUMN work_list.priority IS '1=highest, 4=lowest';
COMMENT ON COLUMN work_list.status IS 'open, in_progress, testing (needs review), done, killed';
COMMENT ON COLUMN work_list.shift IS 'day or night. Night items are worked during overnight crons.';
COMMENT ON COLUMN work_list.manifest_path IS 'Path to work manifest file with instructions and acceptance criteria.';

-- Auto-update trigger for updated_at
CREATE OR REPLACE FUNCTION update_updated_at()
RETURNS TRIGGER AS $$
BEGIN
  NEW.updated_at = now();
  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER work_list_updated_at
  BEFORE UPDATE ON work_list
  FOR EACH ROW EXECUTE FUNCTION update_updated_at();


