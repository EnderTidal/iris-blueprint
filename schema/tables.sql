-- IRIS Blueprint — Database Schema
-- PostgreSQL 16+ with pgcrypto and uuid-ossp extensions
--
-- This schema defines the core tables for an AI executive assistant.
-- All tables are designed for a single-user system. Multi-tenant
-- adaptation would require adding user_id columns and RLS policies.

CREATE EXTENSION IF NOT EXISTS pgcrypto;
CREATE EXTENSION IF NOT EXISTS "uuid-ossp";

-- ============================================================
-- OPERATIONAL STATE — Persistent whiteboard (survives crashes)
-- ============================================================

CREATE TABLE operational_state (
  id         SERIAL PRIMARY KEY,
  key        TEXT NOT NULL UNIQUE,
  value      JSONB NOT NULL,
  updated_at TIMESTAMP DEFAULT now(),
  updated_by TEXT DEFAULT 'IRIS'
);

COMMENT ON TABLE operational_state IS 'Persistent key-value whiteboard. The assistant reads this on startup to restore context.';
COMMENT ON COLUMN operational_state.key IS 'Unique key name (top6, pending_tomorrow, blockers, session_notes, session_texture, active_narratives, etc)';
COMMENT ON COLUMN operational_state.value IS 'JSONB payload. Structure varies by key.';
COMMENT ON COLUMN operational_state.updated_at IS 'Last write timestamp. Check this to know freshness.';

-- Seed standard keys
INSERT INTO operational_state (key, value) VALUES
  ('top6', '{"date": null, "items": []}'),
  ('pending_tomorrow', '{"date": null, "items": [], "committed": false}'),
  ('blockers', '[]'),
  ('session_notes', '{}'),
  ('session_texture', '{}'),
  ('active_narratives', '{}'),
  ('last_closeout', '{}');


-- ============================================================
-- SESSIONS — Session registry
-- ============================================================

CREATE TABLE sessions (
  session_id        UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  role              VARCHAR(20) NOT NULL DEFAULT 'primary',
  started_at        TIMESTAMPTZ NOT NULL DEFAULT now(),
  last_heartbeat    TIMESTAMPTZ NOT NULL DEFAULT now(),
  status            VARCHAR(20) NOT NULL DEFAULT 'active',
  shutdown_type     VARCHAR(20),
  notes             TEXT,
  assigned_channels TEXT[],
  persona           VARCHAR(30),
  config            JSONB DEFAULT '{}'
);

COMMENT ON TABLE sessions IS 'Registry of assistant sessions. Tracks start time, status, assigned channels.';


-- ============================================================
-- MESSAGE SYSTEM — Conversation logging and routing
-- ============================================================

CREATE TABLE message_log (
  id           SERIAL PRIMARY KEY,
  channel      TEXT NOT NULL,
  sender       TEXT NOT NULL,
  message_text TEXT NOT NULL,
  message_type TEXT DEFAULT 'text',
  persona      TEXT,
  timestamp    TIMESTAMPTZ DEFAULT now(),
  session_id   TEXT,
  embedded     BOOLEAN DEFAULT false
);

COMMENT ON TABLE message_log IS 'Raw conversation log. 2-week retention. Nightly embedding compresses to vector DB.';
COMMENT ON COLUMN message_log.channel IS 'Channel name: DM, Professional, Coaching, etc.';
COMMENT ON COLUMN message_log.sender IS 'Who sent: user name for inbound, persona name for outbound';
COMMENT ON COLUMN message_log.embedded IS 'Whether this message has been embedded to the vector DB. false=pending, true=done.';

CREATE INDEX idx_message_log_channel_ts ON message_log (channel, timestamp DESC);
CREATE INDEX idx_message_log_embedded ON message_log (embedded) WHERE embedded = false;


